<template>
  <div class="page">
    <div class="container-wrapper" :style="getBackgroundUrl(currentUser)">
      <v-container ref="cards" v-resize="onResize" fluid>
        <v-row>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Users')"
              icon="mdi-account-group"
              :legend="$t('Total')"
              :value="info.userCount"
              color="red"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Organizations')"
              icon="mdi-domain"
              :legend="$t('Total')"
              :value="info.organizationCount"
              color="blue"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Projects')"
              icon="mdi-home"
              :legend="$t('Total')"
              :value="info.projectCount"
              color="blue"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Tasks')"
              icon="mdi-format-list-bulleted"
              :legend="$t('Total')"
              :value="info.taskCount"
              color="blue"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Attachments')"
              icon="mdi-attachment"
              :legend="$t('Total')"
              :value="info.attachmentCount"
              color="teal"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Diagrams')"
              icon="mdi-chart-donut"
              :legend="$t('Total')"
              :value="info.processDiagramCount"
              color="teal"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('Weather')"
              icon="mdi-weather-cloudy"
              :legend="$t('Total')"
              :value="info.healthReportCount"
              color="blue-grey"
            />
          </v-col>
          <v-col :cols="cardColumns">
            <admin-dashboard-card
              :title="$t('meetings.meetings')"
              icon="mdi-calendar-star"
              :legend="$t('Total')"
              :value="info.meetingCount"
              color="blue-grey"
            />
          </v-col>
        </v-row>
      </v-container>
    </div>
  </div>
</template>

<script>
import { mapState } from "vuex";
import AdminDashboardCard from "./AdminDashboardCard";
import BackgroundMixin from "/imports/ui/mixins/BackgroundMixin.js";

export default {
  components: {
    AdminDashboardCard
  },
  mixins: [BackgroundMixin],
  data() {
    return {
      info: {},
      cardColumns: 4
    };
  },
  computed: {
    ...mapState(["currentUser"])
  },
  mounted() {
    this.refresh();
  },
  methods: {
    async refresh() {
      try {
        const result = await Meteor.callAsync("administration.info", { projectId: this.projectId });
        this.info = result;
      } catch (error) {
        this.$notifyError(error);
      }
    },

    onResize() {
      const { cards } = this.$refs;
      const width = cards.offsetWidth;
      if (width > 600) {
        this.cardColumns = 4;
      } else {
        this.cardColumns = 12;
      }
    }

  }
};
</script>

