<template>
  <div class="new-process-diagram">
    <v-dialog v-model="showDialog" max-width="420" :fullscreen="$vuetify.breakpoint.xsOnly">
      <v-card>
        <v-toolbar dark color="primary">
          <v-btn icon flat @click="close()" v-shortkey="['esc']" @shortkey="close()">
            <v-icon>close</v-icon>
          </v-btn>
          <v-toolbar-title>
            <span>{{ $t('New process diagram') }}</span>
          </v-toolbar-title>
        </v-toolbar>
        <v-card-text>
          <v-form v-model="valid" v-on:submit.prevent>
            <v-layout wrap>
              <v-flex xs12>
                <v-text-field v-model="name" ref="name" :rules="nameRules" label="Nom" required></v-text-field>
              </v-flex>
              <v-flex xs12>
                <label>{{ $t('Description') }}</label>
                <rich-editor v-model="description" ref="description"></rich-editor>
              </v-flex>
            </v-layout>
          </v-form>
        </v-card-text>
        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn flat @click="showDialog = false">{{ $t('Cancel') }}</v-btn>
          <v-btn color="primary" @click="create" :disabled="!valid">{{ $t('Create') }}</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
  </div>
</template>

<script>
import { Meteor } from "meteor/meteor";
import { ProcessDiagrams } from "/imports/api/bpmn/processDiagrams.js";

export default {
  props: {
    projectId: {
      type: String,
      defaultValue: "0"
    }
  },
  i18n: {
    messages: {
      en: {
        "New process diagram": "New process diagram"
      },
      fr: {
        "New process diagram": "Nouveau diagramme"
      }
    }
  },
  data() {
    return {
      showDialog: false,
      valid: false,
      name: "",
      nameRules: [
        v => !!v || "Le nom est obligatoire",
        v => v.length > 1 || "Le nom est trop court"
      ],
      description: ""
    };
  },
  methods: {
    open() {
      this.showDialog = true;
      this.name = "";
      this.$nextTick(() => this.$refs.name.focus());
    },
    close() {
      this.showDialog = false;
    },

    create() {
      this.showDialog = false;
      Meteor.call(
        "processDiagrams.create",
        {
          projectId: this.projectId,
          name: this.name,
          description: this.description
        },
        (error, result) => {
          if (error) {
            this.$store.dispatch("notifyError", error);
            return;
          }
          this.$router.push({
            name: "project-bpmn-process-diagram",
            params: {
              projectId: this.projectId,
              processDiagramId: result
            }
          });
        }
      );
      this.showDialog = false;
    }
  }
};
</script>

