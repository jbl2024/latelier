<template>

<div class="task-attachments">
  <input type="file" v-if="!isUploading" @change="onUpload" :disabled="isUploading"/>
  <v-progress-linear indeterminate v-show="isUploading"></v-progress-linear>
  <v-list>
    <v-list-item v-for="attachment in attachments" :key="attachment._id">
      <v-list-item-avatar>
        <v-icon>mdi-file-document</v-icon>
      </v-list-item-avatar>            

      <v-list-item-content class="pointer">
        <v-list-item-title>
          <a :href="link(attachment)"  target="_blank">{{ attachment.name }}</a>          
        </v-list-item-title>
      </v-list-item-content>

      <v-list-item-action>
        <v-btn icon ripple @click.stop="deleteAttachment(attachment)">
          <v-icon>mdi-delete</v-icon>
        </v-btn>
      </v-list-item-action>
    </v-list-item>
  </v-list> 
</div>

</template>

<script>
import { Projects } from "/imports/api/projects/projects.js";
import { Lists } from "/imports/api/lists/lists.js";
import { Tasks } from "/imports/api/tasks/tasks.js";
import { Attachments } from "/imports/api/attachments/attachments";

import moment from "moment";
import "moment/locale/fr";

export default {
  name: "task-attachments",
  props: {
    task: {
      type: Object
    }
  },
  data() {
    return {
      file: null,
      isUploading: false
    };
  },
  methods: {
    onUpload(e) {
      var file = e.target.files[0];
      var that = this;
      const transport = Meteor.settings.public.uploadTransport || "ddp";
      const upload = Attachments.insert(
        {
          file: file,
          streams: "dynamic",
          chunkSize: "dynamic",
          transport: transport,
          meta: {
            projectId: this.task.projectId,
            taskId: this.task._id,
            createdBy: Meteor.userId()
          }
        },
        false
      );

      upload.on("start", function() {
        that.isUploading = true;
      });

      upload.on("end", function(error, fileObj) {
        that.isUploading = false;
        if (error) {
          alert("Error during upload: " + error);
        } else {
          Meteor.call('tasks.addAttachment', that.task._id);          
          that.file = null;
        }
      });

      upload.start();
    },

    deleteAttachment (attachment) {
      this.$confirm(this.$t("Delete attachment?"), {
        title: attachment.name,
        cancelText: this.$t("Cancel"),
        confirmText: this.$t("Delete")
      }).then(res => {
        if (res) {
          Meteor.call('tasks.removeAttachment', this.task._id, attachment._id);          
        }
      });
    },

    link (attachment) {
      return Attachments.link(attachment);
    }
  },
  meteor: {
    attachments: {
      params () {
        return {
          task: this.task
        };
      },
      update ({task}) {
        return Attachments.find({'meta.taskId': this.task._id});
      }
    }
  }
};
</script>

